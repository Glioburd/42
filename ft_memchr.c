#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t	i;
	char	*s;

	s = (char *)str;
	i = 0;
	while (i < n)
	{
		if (s[i] == c)
		{
			s += i;
			return ((void*)s);
		}
		i++;
	}
	return (NULL);
}
