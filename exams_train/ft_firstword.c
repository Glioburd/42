#include <unistd.h>

int	ft_firstword(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && str[i] != ' ' && str[i] != '	')
	{
		ft_putchar(str[i]);
		i++;
	}
	return (0);
}

